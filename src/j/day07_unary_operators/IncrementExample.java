package j.day07_unary_operators;

public class IncrementExample {
    public static void main(String[] args) {

        int x = 0;
        System.out.println(x);
        System.out.println(x+1);

        x++;                    //post increment
        System.out.println(x);

        ++x;                    //pre increment
        System.out.println(x);




    }
}
